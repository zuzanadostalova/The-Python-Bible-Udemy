# 31. lesson - Booleans
# Boolean is not created through typing its value - we get it from doing logical comparison
print(2<3)
# Output: True

print(2>3)
# Output: False

print(type(2<3))
# Output: <class 'bool'>

# print(2 = 3)
# Output: Error - cannot assign to literal

print(2 == 3)
# Output: False

print(3 == 3)
# Output: True

print(2 != 3)
# Output: True; 2 does not equal to 3

print(4 >= 3)
# Output: True, 4 is bigger or equals 3

print(3 >= 3)
# Output: True

print(2 >= 3)
# Output: False

print(4 <= 3)
# Output: False

print(2 <= 3)
# Output: True

print(3 <= 3)
# Output: True

